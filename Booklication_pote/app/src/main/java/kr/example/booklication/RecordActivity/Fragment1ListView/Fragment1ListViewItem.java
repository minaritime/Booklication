package kr.example.booklication.RecordActivity.Fragment1ListView;

public class Fragment1ListViewItem {

    private String sImage;
    private String sTitle;
    private String sAuthor;
    private String sBeginDate;
    private String sCloseDate;

    public void setImage(String image3) {
        this.sImage = image3;
    }

    public String getImage() {
        return sImage;
    }

    public void setTitle(String title) {
        this.sTitle = title;
    }

    public String getTitle() {
        return sTitle;
    }

    public void setAuthor(String author) {
        this.sAuthor = author;
    }

    public String getAuthor() {
        return sAuthor;
    }

    public void setBeginDate(String begindate) {
        this.sBeginDate = begindate;
    }

    public String getBeginDate() {
        return sBeginDate;
    }

    public void setCloseDate(String closedate) {
        this.sCloseDate = closedate;
    }

    public String getCloseDate() {
        return sCloseDate;
    }
}
